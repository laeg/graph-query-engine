public class Report {

    String mName;
    String mQuery;
    String mTabName;
    String mFilter;
    String mPath;
    String mType;
    String mOrderBy;
    
    /**
     * @return the mOrderBy
     */
    public String getOrderBy() {
        return this.mOrderBy;
    }
    /**
     * @param orderBy the mOrderBy to set
     */
    public void setmOrderBy(String orderBy) {
        this.mOrderBy = orderBy;
    }
    /**
     * @return the mName
     */
    public String getName() {
        return this.mName;
    }
    /**
     * @param mName the mName to set
     */
    public void setName(String name) {
        this.mName = name;
    }
    /**
     * @return the mQuery
     */
    public String getQuery() {
        return this.mQuery;
    }
    /**
     * @param mQuery the mQuery to set
     */
    public void setQuery(String query) {
        this.mQuery = query;
    }
    /**
     * @return the mTabName
     */
    public String getTabName() {
        return this.mTabName;
    }
    /**
     * @param mTabName the mTabName to set
     */
    public void setTabName(String tabName) {
        this.mTabName = tabName;
    }
    /**
     * @return the mFilter
     */
    public String getFilter() {
        return this.mFilter;
    }
    /**
     * @param mFilter the mFilter to set
     */
    public void setFilter(String filter) {
        this.mFilter = filter;
    }
    /**
     * @return the mPath
     */
    public String getPath() {
        return this.mPath;
    }
    /**
     * @param mPath the mPath to set
     */
    public void setPath(String path) {
        this.mPath = path;
    }
    /**
     * @return the mType
     */
    public String getmType() {
        return mType;
    }
    /**
     * @param mType the mType to set
     */
    public void setType(String type) {
        this.mType = type;
    }
}
